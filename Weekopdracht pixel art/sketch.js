function drawGridMario(xPos, yPos, width, height, arrpixelMario) {
 
  for (let i = 0; i < arrpixelMario.length; i++) {
    for (let j = 0; j < arrpixelMario[i].length; j++) {
      switch (arrpixelMario[i][j]) {
        case 0:
          fill("white");
          break;
        case 1:
          fill("Red");
          break;
        case 2:
          fill("green");
          break;
        case 3:
          fill("Yellow");

          break;
        default:
          break;
        
      }
      rect(xPos + j * width, yPos + i * height, width, height);
      
    }
  }
}

